const grade = 'A';
switch(grade){
    case 'A':
        console.log("Excellent");
        break;
        case 'B':
        console.log("Very Good");
        break;
        case 'C':
        console.log("need improvement");
        break;
        case 'D':
        console.log("Fail");
        break;
        default:
            console.log("Invalid Grade");

}
